package com.example.hp1.myfinalproject;

public class Date {
int day,month,year;

public Date(){}

public Date(int day, int month, int year) {
	super();
	this.day = day;
	this.month = month;
	this.year = year;
}

public int getDay() {
	return day;
}


public int getMonth() {
	return month;
}


public int getYear() {
	return year;
}

}
